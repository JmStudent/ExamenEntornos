public class Calculadora {
    
    public int sumar(int uno, int dos) {
        return uno + dos;
    }
    
    public int restar(int uno, int dos) {
        return uno - dos;
    }
    
    public int dividir(int uno, int dos) {
        return uno / dos;
    }
    
    public int multiplicar(int uno, int dos) {
        return uno * dos;
    }
}
